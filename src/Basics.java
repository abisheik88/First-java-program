public class Basics {
    public static void main(String[] args) {
        //While Loop

        int count =0;

        while(count !=5){
            System.out.println(count);
            count++;
        }

        //For loop
        for(int number=1;number!=5;number++){
            System.out.println(number);
        }


        //When to Use For loop and While Loop

        //For Loop:  When we know the number of times the loop is going to run use For loop
        //While Loop: When We Don't know the number of times the loop is going to run we use While loop
    }
}
